r"""
Hierarchical Langevin simulation consistent with type-A nested superstatistics.

The theoretical type-A weights are f_A(beta|n) and h_A(n). Since the
Langevin process samples locally normalized Gaussian distributions, the
environmental variables must be sampled from the corresponding residence
densities

    rho(beta|n) proportional to Z_u(beta) f_A(beta|n),
    rho_n(n)    proportional to Z_A(n) h_A(n).

With these residence densities, the stationary marginal generated by the
Langevin dynamics is the normalized type-A nested distribution.

Outputs
-------
Fig-Langevin.pdf
Fig-Langevin.png
Fig-Langevin-data.npz
Fig-Langevin-parameters.json
"""

